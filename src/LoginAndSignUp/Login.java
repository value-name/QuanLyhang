    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String tenDangNhap = jTextField1.getText(); // Lấy tên đăng nhập
    String matKhau = new String(jPasswordField1.getPassword()); // Lấy mật khẩu từ jPasswordField1

    if (tenDangNhap.isEmpty() || matKhau.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Vui lòng nhập tên đăng nhập và mật khẩu!");
        return;
    }

    try (Connection conn = DBConnect.getConnection()) {
        String sql = "SELECT * FROM TaiKhoan WHERE TenDangNhap = ? AND MatKhau = ?";
        
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, tenDangNhap);
            ps.setString(2, matKhau);
            
            ResultSet rs = ps.executeQuery();
            
            if (rs.next()) {
                String trangThai = rs.getString("TrangThai");
                String loaiTaiKhoan = rs.getString("LoaiTaiKhoan");

                if (trangThai.equals("hoat dong")) {
                    if (loaiTaiKhoan.equals("admin")) {
                        JOptionPane.showMessageDialog(null, "Đăng nhập thành công với quyền Admin!");
                    } else if (loaiTaiKhoan.equals("nhan vien")) {
                        JOptionPane.showMessageDialog(null, "Đăng nhập thành công với quyền Nhân viên!");
                    }
                    if (trangThai.equals("hoat dong")) {
    TrangChu trangChu = new TrangChu(loaiTaiKhoan); // Truyền loại tài khoản
    trangChu.setVisible(true);
    this.dispose(); // Đóng Login
}

                } else {
                    JOptionPane.showMessageDialog(null, "Tài khoản này đã bị khóa!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Tên đăng nhập hoặc mật khẩu không chính xác!");
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Lỗi kết nối cơ sở dữ liệu: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

